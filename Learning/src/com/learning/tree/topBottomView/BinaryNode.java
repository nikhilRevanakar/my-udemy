package com.learning.tree.topBottomView;

public class BinaryNode {
	
	public int value;
	public BinaryNode left;
	public BinaryNode right;
	
	
	public BinaryNode() {
		super();
	}


	public BinaryNode(int value, BinaryNode left, BinaryNode right) {
		super();
		this.value = value;
		this.left = left;
		this.right = right;
	}
	
	
	

}
